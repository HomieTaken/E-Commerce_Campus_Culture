package Entity;

public class TeamApplication {
    private String teamName;
    private String teamEmail;
    private String teamPassword;
    private String teamSchool;
    private String teamApplyTime;

    public String getTeamEmail() {
        return teamEmail;
    }

    public void setTeamEmail(String teamEmail) {
        this.teamEmail = teamEmail;
    }

    public String getTeamName() {
        return teamName;
    }

    public void setTeamName(String teamName) {
        this.teamName = teamName;
    }

    public String getTeamPassword() {
        return teamPassword;
    }

    public void setTeamApplyTime(String teamApplyTime) {
        this.teamApplyTime = teamApplyTime;
    }

    public String getTeamApplyTime() {
        return teamApplyTime;
    }

    public void setTeamPassword(String teamPassword) {
        this.teamPassword = teamPassword;
    }

    public String getTeamSchool() {
        return teamSchool;
    }

    public void setTeamSchool(String teamSchool) {
        this.teamSchool = teamSchool;
    }

}
